/* DQL - Data Query Language */

/* É possível selecionar colunas específicas ou utlizar o '*' para todas as colunas */

/* Estrutura com colunas */

SELECT coluna1, coluna2 FROM nome_tabela;

/* Estrutura com '*' */

SELECT * FROM nome_tabela;

/* Exemplo com colunas */

SELECT NOME, CPF, ENDERECO FROM CLIENTE;

/* Exemplo com '*' */

SELECT * FROM CLIENTE;
